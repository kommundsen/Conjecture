using System.Collections.Generic;
using System.Text;

namespace Conjecture.Generators;

internal static class StrategyEmitter
{
    private static readonly Dictionary<string, string> TypeMap = new()
    {
        ["System.Int32"] = "global::Conjecture.Core.Gen.Integers<int>()",
        ["System.Int64"] = "global::Conjecture.Core.Gen.Integers<long>()",
        ["System.Byte"] = "global::Conjecture.Core.Gen.Integers<byte>()",
        ["System.Boolean"] = "global::Conjecture.Core.Gen.Booleans()",
        ["System.String"] = "global::Conjecture.Core.Gen.Strings()",
        ["System.Double"] = "global::Conjecture.Core.Gen.Doubles()",
        ["System.Single"] = "global::Conjecture.Core.Gen.Floats()",
    };

    internal static string Emit(TypeModel model)
    {
        StringBuilder sb = new();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        if (!string.IsNullOrEmpty(model.Namespace))
        {
            sb.AppendLine("namespace " + model.Namespace + ";");
            sb.AppendLine();
        }

        string fqn = model.FullyQualifiedName;
        string className = model.TypeName + "Arbitrary";

        sb.AppendLine("internal sealed class " + className + " : global::Conjecture.Core.IStrategyProvider<" + fqn + ">");
        sb.AppendLine("{");
        sb.AppendLine("    public global::Conjecture.Core.Generation.Strategy<" + fqn + "> Create() =>");

        if (model.Members.IsEmpty)
        {
            sb.AppendLine("        global::Conjecture.Core.Strategies.Compose<" + fqn + ">(_ => new " + fqn + "());");
        }
        else
        {
            sb.AppendLine("        global::Conjecture.Core.Strategies.Compose<" + fqn + ">(ctx => new " + fqn + "(");

            for (int i = 0; i < model.Members.Length; i++)
            {
                MemberModel member = model.Members[i];
                bool isLast = i == model.Members.Length - 1;
                if (!TypeMap.TryGetValue(member.TypeFullName, out string? genExpr))
                {
                    genExpr = $"/* unsupported type: {member.TypeFullName} */";
                }

                string suffix = isLast ? "));" : ",";
                sb.AppendLine("            ctx.Next(" + genExpr + ")" + suffix);
            }
        }

        sb.AppendLine("}");
        return sb.ToString();
    }
}
